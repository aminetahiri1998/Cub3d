int    wall_check(float x, float y)
{
    int map_x;
    int map_y;
    if (x < 0 || x > WINDOW_WIDTH || y < 0 || y > WINDOW_HEIGHT)
        return 1;
    map_x = floorf(x / TILE_SIZE);
    map_y = floorf(y / TILE_SIZE);
    return (map[map_y][map_x]);
}

void    draw_squar(int x, int y, int color)
{
    int tmp = y;
    int i = 0;
    int j = 0;
    while (i <= TILE_SIZE && x < WINDOW_WIDTH && y < WINDOW_HEIGHT)
    {
        while (j <= TILE_SIZE)
        {
            //mlx_pixel_put(g_data->ptr, g_data->win, x, y, color);
            g_data->matrix[((int)y * WINDOW_WIDTH) + (int)x] = color;
            j++;
            y++;
        }
        j = 0;
        i++;
        x++;
        y = tmp;
    }
}

void draw_map()
{
	int i;
	int j;
	int x;
	int y;

	i = 0;
	j = 0;
	x = 0;
	y = 0;
	while (y <= WINDOW_HEIGHT)
	{
		while (x <= WINDOW_WIDTH)
		{
			if (map[i][j] == 1)
				draw_squar(x, y, 0xFFFFFF);
            else
                draw_squar(x, y, 0x000000);
			x+=TILE_SIZE;
			j++;
		}
		j = 0;
		x = 0;
		y += TILE_SIZE;
		i++;
	}
}